-- 기본 단체 문자 템플릿 시드
-- 각 클리닉에 6개 기본 템플릿을 추가 (이미 같은 이름의 템플릿이 있으면 건너뜀)

WITH defaults(name, content) AS (
  VALUES
    ('정기 검진 안내',
     '안녕하세요 {환자명}님, {병원명}입니다. 마지막 검진 후 시간이 지나 정기 검진을 안내드립니다. 6개월에 한 번 정기 검진으로 건강한 치아를 유지하세요. 예약/문의: 전화 부탁드립니다.'),
    ('예약 리마인더',
     '[{병원명}] {환자명}님, 예정된 진료 예약을 잊지 않으셨나요? 시간 변경이 필요하시면 미리 연락 부탁드립니다. 감사합니다.'),
    ('생일 축하',
     '🎂 {환자명}님, 생일 진심으로 축하드립니다! {병원명} 가족 모두가 행복한 한 해를 응원합니다. 늘 건강하세요.'),
    ('스케일링 안내',
     '{환자명}님 안녕하세요, {병원명}입니다. 건강한 잇몸과 구취 예방을 위해 6개월~1년에 한 번 스케일링을 권장드립니다. 보험 적용도 가능하니 편하게 문의해 주세요.'),
    ('휴진 안내',
     '[{병원명} 휴진 안내] {환자명}님, 다음 기간 동안 휴진합니다. 진료가 필요하시면 미리 예약 부탁드립니다. 양해 부탁드립니다.'),
    ('이벤트/프로모션',
     '[{병원명} 이벤트] {환자명}님께만 드리는 특별 혜택을 안내드립니다. 자세한 내용은 전화 또는 방문 시 확인해 주세요. 감사합니다.')
)
INSERT INTO bulk_sms_templates (clinic_id, name, content, is_default, created_by)
SELECT c.id, d.name, d.content, FALSE, NULL
FROM clinics c
CROSS JOIN defaults d
WHERE NOT EXISTS (
  SELECT 1 FROM bulk_sms_templates t
  WHERE t.clinic_id = c.id AND t.name = d.name
);
