// sido 컬럼 정규화 — 공백/특수자치도 명칭 통일.
function normalizeSido(sido: string): string {
  return sido.trim().replace(/\s+/g, '')
}

// sigungu 컬럼 정규화 — 연속 공백 1칸으로.
function normalizeSigungu(sigungu: string): string {
  return sigungu.trim().replace(/\s+/g, ' ')
}

/**
 * sido + sigungu 조합으로 5자리 LAWD_CD 를 찾는다.
 * 매칭 우선순위:
 *   1) 정확한 (sido, sigungu) — 예: 경기도 / 고양시 덕양구
 *   2) sigungu 의 첫 단어만으로 폴백 — 예: "고양시 덕양구" → "고양시"
 *   3) 실패 시 null (시세 매칭은 자동 스킵)
 */
export function resolveLawdCdFromMap(
  sido: string | null,
  sigungu: string | null
): string | null {
  if (!sido || !sigungu) return null
  const sidoKey = normalizeSido(sido)
  const table = SIDO_TABLES[sidoKey]
  if (!table) return null

  const sigunguKey = normalizeSigungu(sigungu)
  if (table[sigunguKey]) return table[sigunguKey]

  // "고양시 덕양구" → "고양시" 폴백
  const firstWord = sigunguKey.split(' ')[0]
  if (firstWord && table[firstWord]) return table[firstWord]

  return null
}
