/**
 * 날짜가 주말(토/일)인지 확인
 */
export function isWeekend(dateStr: string): boolean {
  const date = new Date(dateStr + 'T00:00:00')
  const day = date.getDay()
  return day === 0 || day === 6
}

/**
 * 법정 공휴일 이름 반환
 * 고정 공휴일 우선, 그다음 음력 기반 공휴일 확인
 */
export function getKoreanHolidayName(dateStr: string): string {
  const monthDay = dateStr.substring(5) // 'MM-DD'
  if (FIXED_HOLIDAY_NAMES[monthDay]) return FIXED_HOLIDAY_NAMES[monthDay]
  if (VARIABLE_HOLIDAY_NAMES[dateStr]) return VARIABLE_HOLIDAY_NAMES[dateStr]
  return '공휴일'
}

/**
 * 두 날짜 범위 내 법정 공휴일 수 계산
 * - 주말 제외 (평일 공휴일만 카운트)
 * - endDate는 오늘 이전이거나 연차 기간 종료일 중 작은 값 사용
 *
 * @param startDate 시작일 ('YYYY-MM-DD')
 * @param endDate 종료일 ('YYYY-MM-DD')
 * @returns 평일 법정 공휴일 수
 */
export function countKoreanHolidaysInRange(startDate: string, endDate: string): number {
  const start = new Date(startDate + 'T00:00:00')
  const end = new Date(endDate + 'T00:00:00')

  if (start > end) return 0

  // 기간에 포함된 연도 범위
  const startYear = start.getFullYear()
  const endYear = end.getFullYear()

  let count = 0
  for (let year = startYear; year <= endYear; year++) {
    const holidays = getKoreanHolidays(year)
    for (const holiday of holidays) {
      // 범위 내에 있는지 확인
      if (holiday >= startDate && holiday <= endDate) {
        // 주말 제외
        if (!isWeekend(holiday)) {
          count++
        }
      }
    }
  }

  return count
}

/**
 * 특정 날짜가 법정 공휴일인지 확인
 */
export function isKoreanHoliday(dateStr: string): boolean {
  const year = parseInt(dateStr.substring(0, 4))
  const holidays = getKoreanHolidays(year)
  return holidays.includes(dateStr)
}
