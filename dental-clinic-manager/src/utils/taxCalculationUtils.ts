// 10,000천원 기준 세액 (초과분 계산의 기준값)
const TAX_AT_10M: number[] = [1507400, 1431570, 1200840, 1170840, 1140840, 1110840, 1080840, 1050840, 1020840, 990840, 960840]

// =====================================================================
// 자녀 세액 공제 (8세 이상 20세 이하)
// =====================================================================

function getChildDeduction(childCount: number): number {
  if (childCount === 0) return 0
  if (childCount === 1) return 12500
  if (childCount === 2) return 29160
  // 3명 이상: 29,160원 + (2명 초과분 × 25,000원)
  return 29160 + (childCount - 2) * 25000
}

// =====================================================================
// 간이세액표 조회 함수
// =====================================================================

/**
 * 간이세액표에서 월급여와 가족수에 해당하는 소득세를 조회
 */
function lookupTaxFromTable(monthlyIncome: number, familyCount: number): number {
  // 가족수는 1~11 범위로 제한
  const effectiveFamilyCount = Math.min(Math.max(familyCount, 1), 11)
  const familyIndex = effectiveFamilyCount - 1

  // 10,000,000원 초과 처리
  if (monthlyIncome > 10000000) {
    return calculateHighIncomeTax(monthlyIncome, familyIndex)
  }

  // 테이블에서 해당 구간 찾기
  for (const row of TAX_TABLE_2024) {
    const [minSalary, maxSalary, taxes] = row
    if (monthlyIncome >= minSalary && monthlyIncome < maxSalary) {
      return taxes[familyIndex]
    }
  }

  // 테이블에서 찾지 못한 경우 (마지막 구간)
  const lastRow = TAX_TABLE_2024[TAX_TABLE_2024.length - 1]
  return lastRow[2][familyIndex]
}

/**
 * 1,000만원 초과 고소득자 세금 계산
 *
 * 10,000천원 초과 ~ 14,000천원 이하:
 *   (10,000,000원인 경우의 해당 세액) + (10,000,000원을 초과하는 금액 중 98%를 곱한 금액의 35% 상당액) + (25,000원)
 *
 * 14,000천원 초과 ~ 28,000천원 이하:
 *   (1천만원인 경우의 해당세액) + (1,397,000원) + (14,000천원을 초과하는 금액 중 98%를 곱한 금액의 38% 상당액)
 *
 * 28,000천원 초과 ~ 30,000천원 이하:
 *   (10,000천원인 경우의 해당세액) + (6,610,600원) + (28,000천원을 초과하는 금액에 98%를 곱한 금액의 40% 상당액)
 *
 * 30,000천원 초과 ~ 45,000천원 이하:
 *   (10,000천원인 경우의 해당세액) + (7,394,600원) + (30,000천원을 초과하는 금액의 40% 상당액)
 *
 * 45,000천원 초과 ~ 87,000천원 이하:
 *   (10,000천원인 경우의 해당세액) + (13,394,600원) + (45,000천원을 초과하는 금액의 42% 상당액)
 *
 * 87,000천원 초과:
 *   (10,000천원인 경우의 해당세액) + (31,034,600원) + (87,000천원을 초과하는 금액의 45% 상당액)
 */
function calculateHighIncomeTax(monthlyIncome: number, familyIndex: number): number {
  const baseTax = TAX_AT_10M[familyIndex]

  if (monthlyIncome <= 14000000) {
    // 10,000천원 초과 ~ 14,000천원 이하
    const excess = monthlyIncome - 10000000
    return baseTax + Math.floor(excess * 0.98 * 0.35) + 25000
  } else if (monthlyIncome <= 28000000) {
    // 14,000천원 초과 ~ 28,000천원 이하
    const excess = monthlyIncome - 14000000
    return baseTax + 1397000 + Math.floor(excess * 0.98 * 0.38)
  } else if (monthlyIncome <= 30000000) {
    // 28,000천원 초과 ~ 30,000천원 이하
    const excess = monthlyIncome - 28000000
    return baseTax + 6610600 + Math.floor(excess * 0.98 * 0.40)
  } else if (monthlyIncome <= 45000000) {
    // 30,000천원 초과 ~ 45,000천원 이하
    const excess = monthlyIncome - 30000000
    return baseTax + 7394600 + Math.floor(excess * 0.40)
  } else if (monthlyIncome <= 87000000) {
    // 45,000천원 초과 ~ 87,000천원 이하
    const excess = monthlyIncome - 45000000
    return baseTax + 13394600 + Math.floor(excess * 0.42)
  } else {
    // 87,000천원 초과
    const excess = monthlyIncome - 87000000
    return baseTax + 31034600 + Math.floor(excess * 0.45)
  }
}

// =====================================================================
// 소득세 계산 (공개 API)
// =====================================================================

/**
 * 월 급여와 가족수에 따른 소득세 계산
 * @param params 세금 조회 파라미터
 * @returns 소득세 금액 (원)
 */
export function calculateIncomeTax(params: TaxLookupParams): number {
  const { monthlyIncome, familyCount, childCount } = params

  // 간이세액표에서 기본 세액 조회
  const baseTax = lookupTaxFromTable(monthlyIncome, familyCount)

  // 자녀 공제 적용 (8세 이상 20세 이하)
  const childDeduction = getChildDeduction(childCount)
  const finalTax = Math.max(0, baseTax - childDeduction)

  return finalTax
}

/**
 * 지방소득세 계산 (소득세의 10%)
 * @param incomeTax 소득세 금액
 * @returns 지방소득세 금액 (원)
 */
export function calculateLocalIncomeTax(incomeTax: number): number {
  return Math.round(incomeTax * 0.1)
}

/**
 * 소득세 + 지방소득세 총액 계산
 */
export function calculateTotalTax(params: TaxLookupParams): {
  incomeTax: number
  localIncomeTax: number
  totalTax: number
} {
  const incomeTax = calculateIncomeTax(params)
  const localIncomeTax = calculateLocalIncomeTax(incomeTax)

  return {
    incomeTax,
    localIncomeTax,
    totalTax: incomeTax + localIncomeTax
  }
}

