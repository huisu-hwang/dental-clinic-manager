/**
 * 세후 실수령액에서 세전 급여를 역산
 * 이진 탐색 방식 사용
 */
export function calculateGrossFromNet(
  targetNetPay: number,
  insuranceDeductions: number,
  nonTaxableAmount: number,
  familyCount: number,
  childCount: number,
  otherDeductions: number = 0
): {
  grossPay: number
  baseSalary: number
  incomeTax: number
  localIncomeTax: number
  totalDeduction: number
} {
  // 초기 추정값 설정
  let low = targetNetPay
  let high = targetNetPay * 2
  let result = targetNetPay + insuranceDeductions + otherDeductions

  // 이진 탐색으로 세전 급여 찾기
  for (let i = 0; i < 50; i++) {
    const mid = Math.floor((low + high) / 2)

    // 과세 소득 계산 (비과세 제외)
    const taxableIncome = mid - nonTaxableAmount

    // 소득세 계산
    const { incomeTax, localIncomeTax } = calculateTotalTax({
      monthlyIncome: taxableIncome,
      familyCount,
      childCount
    })

    // 총 공제액
    const totalDeduction = insuranceDeductions + incomeTax + localIncomeTax + otherDeductions

    // 실수령액
    const netPay = mid - totalDeduction

    if (Math.abs(netPay - targetNetPay) < 10) {
      result = mid
      break
    }

    if (netPay < targetNetPay) {
      low = mid
    } else {
      high = mid
    }

    result = mid
  }

  // 최종 결과 계산
  const taxableIncome = result - nonTaxableAmount
  const { incomeTax, localIncomeTax } = calculateTotalTax({
    monthlyIncome: taxableIncome,
    familyCount,
    childCount
  })
  const totalDeduction = insuranceDeductions + incomeTax + localIncomeTax + otherDeductions

  return {
    grossPay: result,
    baseSalary: result - nonTaxableAmount,
    incomeTax,
    localIncomeTax,
    totalDeduction
  }
}

/**
 * 세전 급여에서 실수령액 계산
 */
export function calculateNetFromGross(
  grossPay: number,
  insuranceDeductions: number,
  nonTaxableAmount: number,
  familyCount: number,
  childCount: number,
  otherDeductions: number = 0
): {
  netPay: number
  incomeTax: number
  localIncomeTax: number
  totalDeduction: number
  taxableIncome: number
} {
  // 과세 소득 계산 (비과세 제외)
  const taxableIncome = grossPay - nonTaxableAmount

  // 소득세 계산
  const { incomeTax, localIncomeTax } = calculateTotalTax({
    monthlyIncome: taxableIncome,
    familyCount,
    childCount
  })

  // 총 공제액
  const totalDeduction = insuranceDeductions + incomeTax + localIncomeTax + otherDeductions

  // 실수령액
  const netPay = grossPay - totalDeduction

  return {
    netPay,
    incomeTax,
    localIncomeTax,
    totalDeduction,
    taxableIncome
  }
}

